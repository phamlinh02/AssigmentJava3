    public void fillToTable() {
        model.setRowCount(0);
       int i=0;
        for (SVThongTin x : lst) {
            model.addRow(new Object[]{x.getMa(), x.getHoTen(), x.getMail(), x.getSdt(), x.getGt(), x.getDchi()});
        }
    }

    public void showDetailt() {
        SVThongTin x = lst.get(index);
        txtMa.setText(x.getMa());
        txtDC.setText(x.getDchi());
        txtEmail.setText(x.getMail());
        txtHoTen.setText(x.getHoTen());
        txtSDT.setText(x.getSdt());
        if (x.getGt().equalsIgnoreCase("nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        tblSV.setRowSelectionInterval(index, index);
        
    }

    public void setNull() {
        txtDC.setText("");
        txtEmail.setText("");
        txtHoTen.setText("");
        txtMa.setText("");
        txtSDT.setText("");
        index = -1;

    }

