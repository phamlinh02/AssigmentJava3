    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            String ma = lst.get(index).getMa();
            if (new StudentDAO().deleteTT(ma)) {
                lst.remove(index);
                fillToTable();
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                if (lst.isEmpty()) {
                    setNull();
                } else {
                    if (index == lst.size()) {
                        index--;
                    }
                    showDetailt();
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Chọn dòng để xóa đê");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (index != -1) {
            txtMa.setEditable(false);
            if (Hepler.checkRong(txtHoTen, "Họ tên không được trống")) {
                return;
            }
            if (Hepler.checkRong(txtEmail, "Họ tên không được trống")) {
                return;
            }
            if (Hepler.checkRong(txtSDT, "Họ tên không được trống")) {
                return;
            }
            if (!rdoNam.isSelected() && !rdoNu.isSelected()) {
                JOptionPane.showMessageDialog(this, "Chưa chọn giới tính");
                return;
            }
            if (txtDC.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Chưa nhập địa chỉ");
                txtDC.requestFocus();
                return;
            }
            SVThongTin x = lst.get(index);
            x.setMa(txtMa.getText());
            x.setDchi(txtDC.getText());
            String gt = rdoNam.isSelected() ? "Nam" : "Nữ";
            x.setGt(gt);
            x.setHoTen(txtHoTen.getText());
            x.setMail(txtEmail.getText());
            x.setSdt(txtSDT.getText());
            if (new StudentDAO().update(x)) {
                JOptionPane.showMessageDialog(this, "Update thành công");
            }
            fillToTable();
        } else {
            JOptionPane.showMessageDialog(this, "Update thất bại");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //        // TODO add your handling code here:
        if (Hepler.checkRong(txtMa, "Mã không được trống")) {
            return;
        }
        if (Hepler.checkRong(txtHoTen, "Họ tên không được trống")) {
            return;
        }
        if (Hepler.checkRong(txtEmail, "Họ tên không được trống")) {
            return;
        }
        if (Hepler.checkRong(txtSDT, "Họ tên không được trống")) {
            return;
        }
        if (!rdoNam.isSelected() && !rdoNu.isSelected()) {
            JOptionPane.showMessageDialog(this, "Chưa chọn giới tính");
            return;
        }
        if (txtDC.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập địa chỉ");
            txtDC.requestFocus();
            return;
        }
        SVThongTin x = new SVThongTin();
        x.setMa(txtMa.getText());
        x.setDchi(txtDC.getText());
        String gt = rdoNam.isSelected() ? "Nam" : "Nữ";
        x.setGt(gt);
        x.setHoTen(txtHoTen.getText());
        x.setMail(txtEmail.getText());
        x.setSdt(txtSDT.getText());
        if (new StudentDAO().add(x)) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            lst.add(x);
            fillToTable();

        } else
        JOptionPane.showMessageDialog(this, "Thêm thất bại");
    }//GEN-LAST:event_btnSaveActionPerformed

